#include <stdio.h>
#include "main.h"

/**
 * _islower - a function that checks for lowercase character
 *
 * Return: 1 if lower and 0 otherwise
 */

int _islower(int c)
{
	for (c = 'a';c <= 'z'; c++)
	{
	return (1);
	}
	if ((c = 'A') && (c <= 'Z'))
	{
	return (0);
	}
	_putchar('\n');
	return (0);

}
